package com.BudgetSpoon.controller;

public class Averages {

	private int base_average;
	private int meal_average;
	private int type_average;
	private int restaurant_id;

	public int getBase_average() {
		return base_average;
	}

	public void setBase_average(int base_average) {
		this.base_average = base_average;
	}

	public int getMeal_average() {
		return meal_average;
	}

	public void setMeal_average(int meal_average) {
		this.meal_average = meal_average;
	}

	public int getType_average() {
		return type_average;
	}

	public void setType_average(int type_average) {
		this.type_average = type_average;
	}

	public int getRestaurant_id() {
		return restaurant_id;
	}
	
	public void setRestaurant_id(int restaurant_id) {
		this.restaurant_id = restaurant_id;
	}

}
